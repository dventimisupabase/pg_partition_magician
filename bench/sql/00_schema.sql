-- Load-test schema: a multi-table OLTP-ish database with one giant append-heavy
-- table (bench.events) that badly wants time partitioning, plus small companions.
create schema if not exists bench;

create table if not exists bench.users (
  id         int  generated by default as identity primary key,
  name       text not null,
  created_at timestamptz not null default now()
);

-- The partition candidate. Heap rows are ~400 B (incompressible payload), so the
-- table reaches the target size with a predictable row count.
create table if not exists bench.events (
  id         bigint generated by default as identity,
  created_at timestamptz not null default now(),
  user_id    int      not null,
  kind       smallint not null,
  payload    text     not null,
  -- the partition key (created_at) is part of the PK, so pgpm reuses it in place at adopt (no PK
  -- rewrite; see DESIGN.md section 8). A bare id PK would be refused for time partitioning now.
  primary key (created_at, id)
);
-- the lookup index real apps keep (per-user recent activity)
create index if not exists events_user_created_idx on bench.events (user_id, created_at desc);

-- a small companion so the workload touches more than the giant table
create table if not exists bench.user_seen (
  user_id    int primary key,
  last_event bigint,
  seen_at    timestamptz not null default now()
);

-- seed a modest user population
insert into bench.users (name)
select 'user_' || g from generate_series(1, 50000) g
on conflict do nothing;
